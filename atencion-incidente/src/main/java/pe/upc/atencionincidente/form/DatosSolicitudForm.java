package pe.upc.atencionincidente.form;

public class DatosSolicitudForm {
	
	private String tipoSolicitud;
	private String sistema;
	private String proceso;
	private String subProceso;
	private String nroUsAfec;
	private String analistaF;
	private String nroCti;
	
	private String id_solicitud;
	
	private String descripcionSol;
	
	
	public String getTipoSolicitud() {
		return tipoSolicitud;
	}
	public void setTipoSolicitud(String tipoSolicitud) {
		this.tipoSolicitud = tipoSolicitud;
	}
	public String getSistema() {
		return sistema;
	}
	public void setSistema(String sistema) {
		this.sistema = sistema;
	}
	public String getProceso() {
		return proceso;
	}
	public void setProceso(String proceso) {
		this.proceso = proceso;
	}
	public String getSubProceso() {
		return subProceso;
	}
	public void setSubProceso(String subProceso) {
		this.subProceso = subProceso;
	}
	public String getNroUsAfec() {
		return nroUsAfec;
	}
	public void setNroUsAfec(String nroUsAfec) {
		this.nroUsAfec = nroUsAfec;
	}
	public String getAnalistaF() {
		return analistaF;
	}
	public void setAnalistaF(String analistaF) {
		this.analistaF = analistaF;
	}
	public String getNroCti() {
		return nroCti;
	}
	public void setNroCti(String nroCti) {
		this.nroCti = nroCti;
	}
	public String getId_solicitud() {
		return id_solicitud;
	}
	public void setId_solicitud(String id_solicitud) {
		this.id_solicitud = id_solicitud;
	}
	
	public String getDescripcionSol() {
		return descripcionSol;
	}
	public void setDescripcionSol(String descripcionSol) {
		this.descripcionSol = descripcionSol;
	}
	
	

}
