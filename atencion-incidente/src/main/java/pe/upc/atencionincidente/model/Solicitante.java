package pe.upc.atencionincidente.model;

public class Solicitante {
	
	String id;
	String matricula;
	String nombresApellidos;
	String area;
	String cargo;
	String anexo;
	String agencia;
	
	String txtArea;
	String txtCargo;
	String txtAgencia;
	
	public String getMatricula() {
		return matricula;
	}
	public void setMatricula(String matricula) {
		this.matricula = matricula;
	}
	public String getNombresApellidos() {
		return nombresApellidos;
	}
	public void setNombresApellidos(String nombresApellidos) {
		this.nombresApellidos = nombresApellidos;
	}
	public String getArea() {
		return area;
	}
	public void setArea(String area) {
		this.area = area;
	}
	public String getCargo() {
		return cargo;
	}
	public void setCargo(String cargo) {
		this.cargo = cargo;
	}
	public String getAnexo() {
		return anexo;
	}
	public void setAnexo(String anexo) {
		this.anexo = anexo;
	}
	public String getAgencia() {
		return agencia;
	}
	public void setAgencia(String agencia) {
		this.agencia = agencia;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getTxtArea() {
		return txtArea;
	}
	public void setTxtArea(String txtArea) {
		this.txtArea = txtArea;
	}
	public String getTxtCargo() {
		return txtCargo;
	}
	public void setTxtCargo(String txtCargo) {
		this.txtCargo = txtCargo;
	}

	public String getTxtAgencia() {
		return txtAgencia;
	}
	public void setTxtAgencia(String txtAgencia) {
		this.txtAgencia = txtAgencia;
	}
	
	
	

}
