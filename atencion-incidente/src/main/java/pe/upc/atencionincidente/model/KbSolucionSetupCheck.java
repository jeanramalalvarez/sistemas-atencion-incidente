package pe.upc.atencionincidente.model;

public class KbSolucionSetupCheck{
		
	private String idSolucion;
	
	private String idTipoSolucion;
	
	private String numSecuencia;
	
	private String idSolucionCh;

	private String usuarioAdicion;
	
	private String solucionesSetupCheck;

	public String getIdSolucion() {
		return idSolucion;
	}

	public void setIdSolucion(String idSolucion) {
		this.idSolucion = idSolucion;
	}

	public String getIdTipoSolucion() {
		return idTipoSolucion;
	}

	public void setIdTipoSolucion(String idTipoSolucion) {
		this.idTipoSolucion = idTipoSolucion;
	}

	public String getNumSecuencia() {
		return numSecuencia;
	}

	public void setNumSecuencia(String numSecuencia) {
		this.numSecuencia = numSecuencia;
	}

	public String getIdSolucionCh() {
		return idSolucionCh;
	}

	public void setIdSolucionCh(String idSolucionCh) {
		this.idSolucionCh = idSolucionCh;
	}

	public String getUsuarioAdicion() {
		return usuarioAdicion;
	}

	public void setUsuarioAdicion(String usuarioAdicion) {
		this.usuarioAdicion = usuarioAdicion;
	}
	
	public String getSolucionesSetupCheck() {
		return solucionesSetupCheck;
	}
	
	public void setSolucionesSetupCheck(String solucionesSetupCheck) {
		this.solucionesSetupCheck = solucionesSetupCheck;
	}
}