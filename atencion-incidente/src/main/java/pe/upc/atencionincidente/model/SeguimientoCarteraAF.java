package pe.upc.atencionincidente.model;

public class SeguimientoCarteraAF {
	
	private String analistaFunciona;
	
	private Integer enProceso;
	
	private Integer asignado;
	
	private Integer total;

	public String getAnalistaFunciona() {
		return analistaFunciona;
	}

	public void setAnalistaFunciona(String analistaFunciona) {
		this.analistaFunciona = analistaFunciona;
	}

	public Integer getEnProceso() {
		return enProceso;
	}

	public void setEnProceso(Integer enProceso) {
		this.enProceso = enProceso;
	}

	public Integer getAsignado() {
		return asignado;
	}

	public void setAsignado(Integer asignado) {
		this.asignado = asignado;
	}

	public Integer getTotal() {
		return total;
	}

	public void setTotal(Integer total) {
		this.total = total;
	}

}
