package pe.upc.atencionincidente.model;

public class Solicitud {
	
	private String id;
	private String nroCti;
	private String estado;
	private String tipoSol;
	private String sistema;
	private String proceso;
	private String fechaReg;
	private String prioridad;
	private String usAfec;
	private String nroSS;
	private String solicitante;
	private String analistaF;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	
	public String getNroCti() {
		return nroCti;
	}
	public void setNroCti(String nroCti) {
		this.nroCti = nroCti;
	}
	public String getEstado() {
		return estado;
	}
	public void setEstado(String estado) {
		this.estado = estado;
	}
	public String getTipoSol() {
		return tipoSol;
	}
	public void setTipoSol(String tipoSol) {
		this.tipoSol = tipoSol;
	}
	public String getSistema() {
		return sistema;
	}
	public void setSistema(String sistema) {
		this.sistema = sistema;
	}
	public String getProceso() {
		return proceso;
	}
	public void setProceso(String proceso) {
		this.proceso = proceso;
	}
	public String getFechaReg() {
		return fechaReg;
	}
	public void setFechaReg(String fechaReg) {
		this.fechaReg = fechaReg;
	}
	public String getPrioridad() {
		return prioridad;
	}
	public void setPrioridad(String prioridad) {
		this.prioridad = prioridad;
	}
	public String getUsAfec() {
		return usAfec;
	}
	public void setUsAfec(String usAfec) {
		this.usAfec = usAfec;
	}
	public String getNroSS() {
		return nroSS;
	}
	public void setNroSS(String nroSS) {
		this.nroSS = nroSS;
	}
	public String getSolicitante() {
		return solicitante;
	}
	public void setSolicitante(String solicitante) {
		this.solicitante = solicitante;
	}
	public String getAnalistaF() {
		return analistaF;
	}
	public void setAnalistaF(String analistaF) {
		this.analistaF = analistaF;
	}
	
	

}
